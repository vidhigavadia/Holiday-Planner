public class NewTripDetail {
    private String area="", htype="";
    private String asdate="", aedate="";
    private int no=0, tripId=0;

    public String getArea() {
        return area;
    }

    public void setArea(String area) {
        this.area = area;
    }

    public String getHtype() {
        return htype;
    }

    public void setHtype(String htype) {
        this.htype = htype;
    }

    public String getAsdate() {
        return asdate;
    }

    public void setAsdate(String asdate) {
        this.asdate = asdate;
    }

    public String getAedate() {
        return aedate;
    }

    public void setAedate(String aedate) {
        this.aedate = aedate;
    }

    public int getNo() {
        return no;
    }

    public void setNo(int no) {
        this.no = no;
    }

    public int getTripId() {
        return tripId;
    }

    public void setTripId(int tripId) {
        this.tripId = tripId;
    }

       
}
